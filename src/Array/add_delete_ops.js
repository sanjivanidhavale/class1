//pop: get the last element of the array and return it

let FruitBasket = ['apple', 'Banana', 'Orange', 'Grapes', 'Mango'];
console.log(FruitBasket);
//pop : remove the last element
console.log(FruitBasket.pop());
console.log(FruitBasket);


//push :add the element to the end of the arry
//
FruitBasket.push('Pear', 'Watermelon')
console.log(FruitBasket);

FruitBasket[FruitBasket.length]='Guava'
console.log(FruitBasket);


//unshift : adding the elment in beginning
FruitBasket.unshift('Strawberry', 'lichi')
console.log(FruitBasket);


//Shift : remove the first element;
FruitBasket.shift();
console.log(FruitBasket);



